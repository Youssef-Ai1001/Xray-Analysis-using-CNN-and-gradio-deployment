import os
import numpy as np
import gradio as gr
import tensorflow as tf
from tensorflow import keras
from PIL import Image
import datetime

# Configuration
MODEL_PATH = "model/pneumonia_detection_Vision_Model.keras"
CLASSES = ["Normal", "Abnormal"]
REPORT_DIR = "reports"

os.makedirs(REPORT_DIR, exist_ok=True)  # Ensure report directory exists

def load_model():
    try:
        model = keras.models.load_model(MODEL_PATH)
        print("âœ… Model loaded successfully!")
        return model
    except Exception as e:
        print(f"âŒ Error loading model: {e}")
        return None

def preprocess_image(image):
    if image.mode != 'RGB':
        image = image.convert('RGB')
    image = image.resize((224, 224))
    img_array = np.array(image) / 255.0
    return np.expand_dims(img_array, axis=0)

def generate_patient_report(patient_name, predicted_class, confidence):
    timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    report_content = (
        f"ğŸ©º Patient Report\n"
        f"--------------------------\n"
        f"ğŸ§‘ Patient Name : {patient_name or 'Anonymous'}\n"
        f"ğŸ—“ï¸ Date         : {timestamp}\n"
        f"ğŸ§ª Result       : {predicted_class}\n"
        f"ğŸ“Š Confidence   : {confidence:.2f}%\n"
        f"--------------------------\n"
        f"ğŸ“Œ Note: This prediction is generated by a deep learning model. Please consult a medical professional for final diagnosis.\n"
    )
    filename = f"{REPORT_DIR}/Report_{patient_name or 'Anonymous'}_{timestamp.replace(':', '-')}.txt"
    with open(filename, "w", encoding='utf-8') as f:
        f.write(report_content)
    return filename, report_content

def predict_xray(image, patient_name):
    if image is None:
        print("âš ï¸ No image provided!")
        return {"Error": "No image provided"}, None
    
    global model
    if 'model' not in globals() or model is None:
        print("ğŸ” Loading model...")
        model = load_model()
        if model is None:
            print("âŒ Model loading failed.")
            return {"Error": "Failed to load model"}, None

    try:
        print("ğŸ–¼ï¸ Preprocessing image...")
        processed_image = preprocess_image(image)
        print("âœ… Image preprocessed.")

        print("ğŸ¤– Making prediction...")
        prediction = model.predict(processed_image)
        print(f"ğŸ“ˆ Raw prediction output: {prediction}")

        prediction_value = prediction[0][0] if prediction.ndim == 2 else prediction[0]
        class_index = 1 if prediction_value > 0.5 else 0
        confidence = prediction_value if class_index == 1 else 1 - prediction_value
        predicted_class = CLASSES[class_index]

        print(f"âœ… Predicted: {predicted_class} - Confidence: {confidence * 100:.2f}%")

        report_file, report_content = generate_patient_report(patient_name, predicted_class, confidence * 100)
        print(f"ğŸ“„ Report saved at: {report_file}")

        result = {
            "Class": predicted_class,
            "Confidence": f"{confidence * 100:.2f}%",
            "Message": "ğŸ“ Patient report generated successfully!"
        }

        return result, report_file

    except Exception as e:
        print(f"âŒ Exception occurred: {str(e)}")
        return {"Error": str(e)}, None


